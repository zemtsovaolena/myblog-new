@app.route('/register', methods=['GET', 'POST'])
def register():
    register_form = RegisterForm()
    if register_form.validate_on_submit():
        email = register_form.email.data
        password = register_form.password.data
        name = register_form.name.data
        if db.session.query(User).filter_by(email=email).first():
            flash("This email is already taken. Please log in.", "error")
            return redirect(url_for("login"))
        else:
            new_user = User(email=email,
                            password=generate_password_hash(password, method="pbkdf2:sha256", salt_length=8), name=name)
            db.session.add(new_user)
            db.session.commit()
            return redirect(url_for("login"))
    return render_template("register.html", form=register_form)


@app.route('/login', methods=['GET', 'POST'])
def login():
    login_form = LoginForm()
    if login_form.validate_on_submit():
        email = login_form.email.data
        if db.session.query(User).filter_by(email=email).first():
            user = db.session.query(User).filter_by(email=email).first()
            password = login_form.password.data
            if check_password_hash(user.password, password):
                login_user(user=user)
                return redirect(url_for('get_all_posts'))
            else:
                flash("The password incorrect try again.", "error")
                return redirect(url_for('login'))
        else:
            flash("This email was not found, please try again.", "error")
            return redirect(url_for('login'))
    return render_template("login.html", form=login_form)


